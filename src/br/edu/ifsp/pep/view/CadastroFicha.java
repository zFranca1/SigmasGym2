    private void btnCadastrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCadastrarMouseClicked
        // TODO add your handling code here:

        FichaTreino ficha = new FichaTreino();

        ficha.setNome(txtNome.getText());
        ficha.setExerciciosFicha(exerciciosFicha);
        for (ExercicioFicha exercicio : ficha.getExerciciosFicha()) {
            exercicio.setFichaTreino(ficha);
        }
        ficha.setUsuario((Usuario) ComboAlunos.getSelectedItem());

        FichaTreinoDAO fichaDAO = new FichaTreinoDAO();

        fichaDAO.insert(ficha);

//        atualiza();

    }//GEN-LAST:event_btnCadastrarMouseClicked

