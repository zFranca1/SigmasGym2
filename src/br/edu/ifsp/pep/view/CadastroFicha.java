    public void atualiza() {

        DefaultTableModel model = (DefaultTableModel) TabelaExercicios.getModel();

        while (model.getRowCount() > 0) {//pega a quantidade de linhas da tabela
            model.removeRow(0);
        }

        for (ExercicioFicha ef : exerciciosFicha) {

            model.addRow(new Object[]{ef.getCodigo(), ef.getRepeticoes(), ef.getSeries(), ef.getCarga(), ef.getIntervalo()});
        }

    }

